ALPHABET_REG = list(map(lambda x: re.escape(x), ALPHABET))

def clean_string(s):
    for pair in REPLACE_PAIRS:
        s = s.replace(pair[0], pair[1])
    s = ' '.join(s.split())

    pattern = re.compile(f"[^{''.join(ALPHABET_REG)}]")
    s = re.sub(pattern, BLANK_CHAR, s)

    return s